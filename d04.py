entrada = input()

print(type(entrada))
print(entrada.isspace()) #só espaços
print(entrada.isnumeric()) #numérico
print(entrada.isalpha()) #alfabético
print(entrada.isalnum()) #alfanumérico
print(entrada.isupper()) #maiúsculo
print(entrada.islower()) #minúsculo
print(entrada.istitle()) #capitalizado